const bcrypt = require('bcryptjs')
const db = require('../models')
const User = db.User
const Comment = db.Comment
const Restaurant = db.Restaurant
const Favorite = db.Favorite
const Like = db.Like
const helpers = require('../_helpers')
const imgur = require('imgur-node-api')
const IMGUR_CLIENT_ID = process.env.IMGUR_CLIENT_ID

const userController = {
  signUpPage: (req, res) => {
    return res.render('signup')
  },

  signUp: (req, res) => {
    const { name, email, password, passwordCheck } = req.body
    if (passwordCheck !== password) {
      req.flash('error_msg', '密碼與確認密碼不相符！')
      return res.redirect('/signup')
    } else {
      User.findOne({
        where: { email }
      })
        .then(user => {
          if (user) {
            req.flash('error_msg', '此信箱已註冊！')
            return res.redirect('/signup')
          }
        })
    }

    User.create({
      name,
      email,
      password: bcrypt.hashSync(password, bcrypt.genSaltSync(10), null)
    })
      .then(user => {
        req.flash('success_msg', '成功註冊帳號！')
        return res.render('signin')
      })
      .catch(err => console.log(err))
  },

  signInPage: (req, res) => {
    return res.render('signin')
  },

  signIn: (req, res) => {
    req.flash('success_msg', '登入成功！')
    res.redirect('/restaurants')
  },

  logout: (req, res) => {
    req.flash('success_msg', '成功登出！')
    req.logout()
    res.redirect('/signin')
  },

  getUser: (req, res) => {
    return User.findByPk(req.params.id)
      .then(user => {
        Comment.findAndCountAll({
          raw: true, nest: true,
          where: { userId: Number(req.params.id) },
          include: Restaurant
        })
          .then(comments => {
            return res.render('profile', {
              userProfile: user.toJSON(),
              count: comments.count,
              comments: comments.rows
            })
          })

      })
      .catch(err => console.log(err))
  },

  editUser: (req, res) => {
    const currentUserId = helpers.getUser(req).id
    return User.findByPk(req.params.id)
      .then(user => {
        // you can only see the edit page of your own profile 
        if (currentUserId !== Number(req.params.id)) {
          req.flash('error_msg', '使用者只能編輯自己的個人資料')
          return res.redirect(`/users/${currentUserId}/edit`)
        }

        return res.render('editProfile', { userProfile: user.toJSON() })
      })
      .catch(err => console.log(err))
  },

  putUser: (req, res) => {
    const { name } = req.body
    const id = req.params.id
    const file = req.file

    if (file) {
      imgur.setClientID(IMGUR_CLIENT_ID);
      imgur.upload(file.path, (err, img) => {
        return User.findByPk(id)
          .then(user => {
            user.update({
              name,
              avatar: file ? img.data.link : user.avatar
            })
              .then(user => {
                req.flash('success_msg', '成功編輯使用者資訊！')
                return res.redirect(`/users/${id}`)
              })
          })
      })
    } else {
      return User.findByPk(id)
        .then(user => {
          user.update({
            name, avatar: user.avatar
          })
            .then(user => {
              req.flash('success_msg', '成功編輯使用者資訊！')
              return res.redirect(`/users/${id}`)
            })
        })
        .catch(err => console.log(err))
    }
  },

  addFavorite: (req, res) => {
    return Favorite.create({
      UserId: helpers.getUser(req).id,
      RestaurantId: req.params.restaurantId
    })
      .then(restaurant => {
        return res.redirect('back')
      })
      .catch(err => console.log(err))
  },

  removeFavorite: (req, res) => {
    return Favorite.findOne({
      where: {
        UserId: helpers.getUser(req).id,
        RestaurantId: req.params.restaurantId
      }
    })
      .then(favorite => {
        favorite.destroy()
          .then(restaurant => {
            return res.redirect('back')
          })
          .catch(err => console.log(err))
      })
      .catch(err => console.log(err))
  },

  addLike: (req, res) => {
    return Like.create({
      UserId: helpers.getUser(req).id,
      RestaurantId: req.params.restaurantId
    })
      .then(restaurant => {
        return res.redirect('back')
      })
      .catch(err => console.log(err))
  },

  removeLike: (req, res) => {
    return Like.findOne({
      where: {
        UserId: helpers.getUser(req).id,
        RestaurantId: req.params.restaurantId
      }
    })
      .then(restaurant => {
        restaurant.destroy()
          .then(restaurant => {
            return res.redirect('back')
          })
          .catch(err => console.log(err))
      })
      .catch(err => console.log(err))
  }
}

module.exports = userController